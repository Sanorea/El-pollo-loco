/**
 * Class representing the state of the keyboard keys.
 * This class tracks the state of various keys (LEFT, RIGHT, UP, DOWN, SPACE, D) and whether they are pressed or not.
 * It is used to detect keyboard inputs in the game or application.
 *
 * @class
 */
class Keyboard {

    /**
     * Indicates whether the LEFT arrow key is pressed.
     * @type {boolean}
     * @default false
     */
    LEFT = false;

    /**
     * Indicates whether the RIGHT arrow key is pressed.
     * @type {boolean}
     * @default false
     */
    RIGHT = false;

    /**
     * Indicates whether the UP arrow key is pressed.
     * @type {boolean}
     * @default false
     */
    UP = false;

    /**
     * Indicates whether the DOWN arrow key is pressed.
     * @type {boolean}
     * @default false
     */
    DOWN = false;

    /**
     * Indicates whether the SPACE key is pressed.
     * @type {boolean}
     * @default false
     */
    SPACE = false;

    /**
     * Indicates whether the D key is pressed.
     * @type {boolean}
     * @default false
     */
    D = false;
}
